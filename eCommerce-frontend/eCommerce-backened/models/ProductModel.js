const mongoose = require("mongoose");

// Creating a product model in order to post what we want into our database
const productSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    slug: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    quantity: {
      type: Number,
      required: true,
    },
    sold: {
      type: Number,
      default: 0,
    },
    images: {
      type: Array,
    },
    color: {
      type: String,
    },
  },
  {
    timestamps: true,
  }
);

// Exporting UserTemplate as a model for our database.
module.exports = mongoose.model("Product", productSchema);
